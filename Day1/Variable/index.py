# Numeric Data Types
a = 5
print(type(a))

b = 5.0
print(type(b))

c = 2 + 4j
print(type(c))

# Sequence Data Types

s = 'Welcome to the Geeks World'
print(s)
# access string with index
print(s[1])
print(s[2])
print(s[-1]) # -1 refers to the last character, -2 is second last, and so on

# List Data Type

# Empty list
a = []

# list with int values
a = [1, 2, 3]
print(a)

# list with mixed values int and String
b = ["Geeks", "For", "Geeks", 4, 5]
print(b)